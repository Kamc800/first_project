age = 11
height = 5.8
# Addition
print(age + height)
# Subtraction
print(age - height)
# Multiplication
print(age * height)
# Division
print(age / height)
# Modulo
print(age % height)
# Floor Division
print(age // height)
# Exponentiation
print(age ** height)
# Comparison Operators
# Equal to (==)
print(age == height)
# Not Equal to (!=)
print(age != height)
# Greater than
print(age > height)
# Less than
print(age < height)
# Greater than or equal to
print(age >= height)
# Less than or equal to
print(age < height)